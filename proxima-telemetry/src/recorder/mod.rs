/// Wired span builder — SpanBuilder pre-loaded with the per-core sink.
///
/// Wraps C5's SpanBuilder with a closure that pushes SpanRecord into the
/// per-core span ring. Zero heap allocation on the emit path; the SpanRecord
/// itself may allocate (attrs Vec, events Vec) as part of normal C5 behavior.
pub struct SpanBuilderWired {
    inner: SpanBuilder,
    shared: Arc<EmitShared>,
    clock: Arc<dyn Clock + Send + Sync>,
    scope_tags: alloc::vec::Vec<Tag>,
    // sampling is decided eagerly in `span()` (all built-in samplers are
    // stateless pure fns of the name), so the guard path never clones the
    // sampler Arc — that clone was a contended cache line on the emit hot path.
    sampled: bool,
    overflow: OverflowPolicy,
    // C5 tail-sampling: if set and the span outruns this many ns, its trace is
    // force-kept even when the head sampler dropped it — the slow path is the one
    // worth a trace. `None` = pure head sampling.
    budget_ns: Option<u64>,
}

impl SpanBuilderWired {
    fn new(
        inner: SpanBuilder,
        shared: Arc<EmitShared>,
        clock: Arc<dyn Clock + Send + Sync>,
        scope_tags: alloc::vec::Vec<Tag>,
        sampled: bool,
        overflow: OverflowPolicy,
    ) -> Self {
        Self {
            inner,
            shared,
            clock,
            scope_tags,
            sampled,
            overflow,
            budget_ns: None,
        }
    }

    /// C5: arm tail-sampling — if the span runs longer than `budget_ns`, its trace
    /// is kept even if the head sampler dropped it (a slow outlier is exactly the
    /// trace worth keeping). The duration metric records regardless. Drives the
    /// `#[span(budget = <ns>)]` macro arg.
    #[must_use]
    pub fn budget(mut self, budget_ns: u64) -> Self {
        self.budget_ns = Some(budget_ns);
        self
    }

    pub fn kind(mut self, kind: crate::trace::SpanKind) -> Self {
        self.inner = self.inner.kind(kind);
        self
    }

    /// Set span kind from a static string (`"server" | "client" | "producer" |
    /// "consumer" | "internal"`); anything else falls back to `Internal`. Lets the
    /// `#[span(kind = "...")]` macro set the kind without naming `SpanKind`.
    #[must_use]
    pub fn kind_str(self, kind: &'static str) -> Self {
        let parsed = match kind {
            "server" => crate::trace::SpanKind::Server,
            "client" => crate::trace::SpanKind::Client,
            "producer" => crate::trace::SpanKind::Producer,
            "consumer" => crate::trace::SpanKind::Consumer,
            _ => crate::trace::SpanKind::Internal,
        };
        self.kind(parsed)
    }

    pub fn parent(mut self, parent: SpanId) -> Self {
        self.inner = self.inner.parent(parent);
        self
    }

    pub fn tag(mut self, key: &'static str, value: impl Into<ScalarValue>) -> Self {
        self.inner = self.inner.tag(key, value);
        self
    }

    pub fn module_path(mut self, mp: &'static str) -> Self {
        self.inner = self.inner.module_path(mp);
        self
    }

    pub fn file_line(mut self, line: u32, col: u32) -> Self {
        self.inner = self.inner.file_line(line, col);
        self
    }

    /// Start the span and return a RAII guard that emits on drop.
    ///
    /// Consults the recorder's sampler BEFORE allocating any SpanRecord state.
    /// If the sampler returns `Decision::Drop`, returns `RecorderSpanGuard::Noop`
    /// — zero allocation, zero ring push.
    pub fn start(self) -> RecorderSpanGuard {
        self.start_with(false)
    }

    /// Like [`start`](Self::start), but the active guard does NOT enter the
    /// current-span stack — for a span that rides an `async` future, where
    /// [`Spanned::scoped`](crate::spanned::Spanned::scoped) manages current-span
    /// per poll. `#[proxima::instrument]` on an `async fn` uses this.
    pub fn start_deferred(self) -> RecorderSpanGuard {
        self.start_with(true)
    }

    fn start_with(self, defer: bool) -> RecorderSpanGuard {
        // sampling already decided in span() (no sampler Arc on the hot path).
        if !self.sampled {
            // C5: a budgeted span builds its record even when head-sampled out, so a
            // slow outlier (duration > budget) can force-keep its trace at close. The
            // recorder's config default applies when no explicit `.budget()` is set —
            // read HERE (the sampled-out path), never on the common sampled-in path.
            let budget = self.budget_ns;
            #[cfg(feature = "instrument-metrics")]
            let budget = budget.or_else(|| match self.shared.default_budget_ns() {
                0 => None,
                default => Some(default),
            });
            if let Some(budget_ns) = budget {
                return self.into_active(KeepPolicy::OverBudget(budget_ns), defer);
            }
            // C2: no budget — the trace is dropped but the metric pillar stays on
            // (when consumed), so a metric-only guard records the duration on drop,
            // no SpanRecord alloc. No metric consumer → no guard at all (Noop).
            #[cfg(feature = "instrument-metrics")]
            if self.shared.span_metrics_live() {
                let start_ns = self.clock.now_ns();
                return RecorderSpanGuard::MetricOnly(MetricOnlyGuard {
                    name: self.inner.name(),
                    shared: self.shared,
                    clock: self.clock,
                    start_ns,
                });
            }
            return RecorderSpanGuard::Noop;
        }

        self.into_active(KeepPolicy::Always, defer)
    }

    /// Build the active span guard with the given trace keep-policy. Shared by the
    /// head-sampled-in path (`Always`) and the C5 tail-sampling path (`OverBudget`).
    /// `defer` selects `enter_deferred` (async, per-poll current-scoping) over
    /// `enter` (sync, scope-lifetime current).
    fn into_active(self, keep: KeepPolicy, defer: bool) -> RecorderSpanGuard {
        // one clock Arc (moved out of self) — used by-ref for the start read and
        // then moved into the guard for the duration read; no second clone.
        let clock = self.clock;
        let sink = RingSink {
            shared: self.shared,
            overflow: self.overflow,
            keep,
        };

        let mut builder = self.inner;
        for tag in self.scope_tags {
            builder = match tag {
                Tag::Scalar { key, value } => builder.tag(key, value),
                Tag::Structured { .. } => builder,
            };
        }

        let span = builder.start(&clock, sink);
        let guard = if defer {
            span.enter_deferred(clock)
        } else {
            span.enter(clock)
        };
        RecorderSpanGuard::Active(guard)
    }
}

