    // full bidirectional W3C: a local span continues an inbound trace, injects
    // its own context outbound, and a downstream span extracted from those
    // headers parents on the local span with the trace_id + tracestate intact.
    #[test]
    fn inject_then_extract_round_trips_w3c() {
        use crate::id::{SpanId, TraceFlags, TraceId};
        use crate::pipes::InMemoryPipe;
        use proxima_primitives::pipe::header_list::HeaderList;
        use crate::propagation::{TRACEPARENT, TRACESTATE};

        let pipe = InMemoryPipe::new();
        let recorder = Recorder::builder()
            .pipe(pipe.clone())
            .core_count(1)
            .start()
            .expect("recorder build failed");

        let origin_trace = TraceId::from_bytes([0x33; 16]);
        let origin_span = SpanId::from_bytes([0x44; 8]);
        let inbound =
            crate::id::format_traceparent(&origin_trace, &origin_span, TraceFlags::SAMPLED);

        let local = recorder
            .span_from_w3c("local", &inbound, Some(b"vendor=abc123"))
            .start();
        let local_span_id = local.id().expect("active span has an id");

        let mut headers = HeaderList::new();
        local.inject(&mut headers);
        let traceparent = headers
            .get(TRACEPARENT)
            .expect("traceparent written")
            .clone();
        let tracestate = headers.get(TRACESTATE).map(|value| value.to_vec());
        drop(local);

        drop(
            recorder
                .span_from_w3c("downstream", traceparent.as_ref(), tracestate.as_deref())
                .start(),
        );
        recorder.drain();

        let spans = pipe.spans();
        let child = spans
            .iter()
            .find(|span| span.parent_span_id == Some(local_span_id))
            .expect("downstream span parents on the local span");
        assert_eq!(
            child.trace_id, origin_trace,
            "trace_id propagates end to end"
        );
        assert_eq!(
            child.tracestate.0.as_deref(),
            Some(b"vendor=abc123".as_ref()),
            "tracestate survives inject then extract"
        );
    }

    // the async size-trigger: a PumpWait future is Pending with no signal, wakes
    // its task + resolves once a signal fires, and a fresh wait is Pending again
    // (the signal was consumed). This is the mechanism a prime pump races against
    // its flush-interval timer for prompt burst draining.
    #[cfg(feature = "lossless-backpressure")]
    #[test]
    fn pump_wait_is_pending_until_signalled_then_consumes_the_signal() {
        use std::task::Wake;

        struct CountingWaker(AtomicU64);
        impl Wake for CountingWaker {
            fn wake(self: Arc<Self>) {
                self.0.fetch_add(1, Ordering::SeqCst);
            }
        }

        let recorder = make_recorder_null(1);
        let woken = Arc::new(CountingWaker(AtomicU64::new(0)));
        let waker = core::task::Waker::from(Arc::clone(&woken));
        let mut context = core::task::Context::from_waker(&waker);

        let mut wait = Box::pin(recorder.pump_wait());
        assert_eq!(
            core::future::Future::poll(wait.as_mut(), &mut context),
            core::task::Poll::Pending,
            "no signal yet — the pump parks on its timer"
        );

        recorder.signal_pump();
        assert!(
            woken.0.load(Ordering::SeqCst) >= 1,
            "signal_pump woke the registered task waker"
        );
        assert_eq!(
            core::future::Future::poll(wait.as_mut(), &mut context),
            core::task::Poll::Ready(()),
            "resolves after the signal — the pump drains immediately"
        );

        let mut wait_again = Box::pin(recorder.pump_wait());
        assert_eq!(
            core::future::Future::poll(wait_again.as_mut(), &mut context),
            core::task::Poll::Pending,
            "the signal was consumed; a fresh wait parks again"
        );

        // the real size trigger: a full-ring producer calls notify_data_ready,
        // which must wake the async pump (not just signal_pump / shutdown).
        recorder.shared.notify_data_ready();
        assert_eq!(
            core::future::Future::poll(wait_again.as_mut(), &mut context),
            core::task::Poll::Ready(()),
            "a full-ring producer signal (notify_data_ready) wakes the async pump"
        );
    }
