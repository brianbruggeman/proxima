    // Pump-active backpressure (the correct async-terminal overflow path): a tiny
    // ring under multi-producer contention forces producers to PARK for the pump
    // rather than self-export. Proves the park branch is lossless, takes the park
    // path (parked() > 0, assisted() == 0 — producers waited on the pump, never
    // block_on'd the sink themselves), never drops, and does not deadlock (the
    // test completes; terminate-after in .config/nextest.toml fails a regression).
    #[cfg(feature = "lossless-backpressure")]
    #[test]
    fn pump_active_parks_producers_losslessly() {
        let (pipe, spans, _events, _logs, _metrics, _links) = CountingPipe::new();
        let recorder = alloc::sync::Arc::new(
            Recorder::builder()
                .pipe(pipe)
                .core_count(1)
                .ring_capacity(2)
                .overflow(OverflowPolicy::Block)
                .managed_drainer(true)
                .start()
                .expect("recorder"),
        );

        let producers = 4u64;
        let per = 2_000u64;
        let handles: alloc::vec::Vec<_> = (0..producers)
            .map(|_| {
                let recorder = alloc::sync::Arc::clone(&recorder);
                std::thread::spawn(move || {
                    for _ in 0..per {
                        drop(recorder.span("op").start());
                    }
                })
            })
            .collect();
        for handle in handles {
            handle.join().expect("producer join");
        }

        assert_eq!(recorder.dropped(), 0, "Block must never drop");
        assert_eq!(
            recorder.assisted(),
            0,
            "pump active => producers park for the pump, never self-assist"
        );
        assert!(
            recorder.parked() > 0,
            "a cap-2 ring under 4 producers must force at least one park"
        );

        let recorder = alloc::sync::Arc::try_unwrap(recorder)
            .ok()
            .expect("sole owner at teardown");
        drop(recorder); // stop+join pump, then EmitShared::drop final flush
        assert_eq!(
            spans.load(Ordering::Relaxed),
            producers * per,
            "every parked-then-resumed span exported losslessly"
        );
    }

