    // The lossless guarantee: under Block (default), emit far past tiny rings with
    // a drainer running — every signal (spans, logs, metrics) must finish with
    // dropped() == 0. The producers backpressure instead of shedding. This is the
    // exact scenario the old drop-on-full policy lost data in.
    #[cfg(feature = "lossless-backpressure")]
    #[test]
    fn block_policy_never_drops_under_saturation() {
        let recorder = alloc::sync::Arc::new(
            Recorder::builder()
                .pipe(NullPipe::new())
                .core_count(2)
                .ring_capacity(16)
                .metric_capacity(16)
                .overflow(OverflowPolicy::Block)
                .start()
                .expect("recorder"),
        );

        let drain_stop = alloc::sync::Arc::new(std::sync::atomic::AtomicBool::new(false));
        let drainer = {
            let recorder = alloc::sync::Arc::clone(&recorder);
            let stop = alloc::sync::Arc::clone(&drain_stop);
            std::thread::spawn(move || {
                while !stop.load(Ordering::Relaxed) {
                    recorder.drain();
                }
                recorder.drain();
            })
        };

        let per_producer = 20_000;
        let producers: alloc::vec::Vec<_> = (0..2)
            .map(|_| {
                let recorder = alloc::sync::Arc::clone(&recorder);
                std::thread::spawn(move || {
                    for _ in 0..per_producer {
                        drop(recorder.span("op").start());
                        recorder.log().message("m").emit();
                        recorder.emit_counter_add("c", 1, &[]);
                    }
                })
            })
            .collect();

        for producer in producers {
            producer.join().expect("producer");
        }
        drain_stop.store(true, Ordering::Relaxed);
        drainer.join().expect("drainer");

        assert_eq!(
            recorder.dropped(),
            0,
            "Block policy must never drop a record"
        );
    }

    // The deadlock-free guarantee: Block (the default) with NO separate drainer.
    // A single producer emits far past a tiny ring; elastic producer-assist must
    // self-drain+export so the loop COMPLETES (proving forward progress without
    // any drainer) AND nothing is dropped. The counting pipe must have received
    // every span — lossless, not merely "didn't deadlock". This is the exact
    // scenario the old naive Block hung in.
    #[cfg(feature = "lossless-backpressure")]
    #[test]
    fn block_no_drainer_self_progresses_no_hang() {
        let (pipe, spans, _events, _logs, _metrics, _links) = CountingPipe::new();
        let recorder = Recorder::builder()
            .pipe(pipe)
            .core_count(1)
            .ring_capacity(16)
            .overflow(OverflowPolicy::Block)
            .start()
            .expect("recorder");

        let emit = 10_000u64;
        for _ in 0..emit {
            drop(recorder.span("op").start());
        }
        // flush whatever assist left in the ring on the final push.
        while recorder.drain() > 0 {}

        assert_eq!(recorder.dropped(), 0, "Block must never drop");
        assert_eq!(
            spans.load(Ordering::Relaxed),
            emit,
            "every span exported losslessly by producer-assist + final drain"
        );
        // the observability signal: with no drainer, the producer self-assisted,
        // so assisted() is the (nonzero) count of records it exported itself —
        // the leading "drain isn't keeping up" indicator, while dropped() is 0.
        assert!(
            recorder.assisted() > 0,
            "producer-assist must register on the assisted() counter"
        );
        assert!(
            recorder.assisted() <= emit,
            "assisted is bounded by records emitted"
        );
    }

