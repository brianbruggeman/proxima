impl<State> RecorderBuilder<State> {
    pub fn ring_capacity(mut self, spans: usize) -> Self {
        self.ring_caps.spans = spans;
        self.ring_caps.events = spans;
        self.ring_caps.logs = spans;
        self
    }

    pub fn metric_capacity(mut self, metrics: usize) -> Self {
        self.ring_caps.metrics = metrics;
        self
    }

    /// Bulk-set the full set of ring capacities. Used by `Recorder::
    /// from_config` to push a `TelemetryConfig`-sourced layout in one
    /// shot, without going through six individual builder verbs.
    pub fn ring_capacities(mut self, caps: RingCapacities) -> Self {
        self.ring_caps = caps;
        self
    }

    /// Number of per-core rings to shard emitting threads across. This is a
    /// throughput knob, not a safety limit: the rings are multi-producer, so
    /// emitting from more threads than `count` is correct (those threads just
    /// share a ring and contend on its push CAS). Size it toward peak concurrent
    /// emitter threads to minimise that contention; defaults to
    /// `available_parallelism`.
    pub fn core_count(mut self, count: usize) -> Self {
        self.core_count = count.max(1);
        self
    }

    pub fn clock(mut self, clock: impl Clock + Send + Sync + 'static) -> Self {
        self.clock = Some(Arc::new(clock));
        self
    }

    pub fn resource_tag(mut self, key: &'static str, value: impl Into<ScalarValue>) -> Self {
        self.resource_tags.push(Tag::Scalar {
            key,
            value: value.into(),
        });
        self
    }

    pub fn sampler(mut self, sampler: impl Sampler + 'static) -> Self {
        self.sampler = Some(alloc::boxed::Box::new(sampler));
        self
    }

    pub fn sampler_boxed(mut self, sampler: alloc::boxed::Box<dyn Sampler>) -> Self {
        self.sampler = Some(sampler);
        self
    }

    /// Set the record sharing policy used at drain time. `Arc` (default)
    /// wraps each record in `Arc<T>` so fanout is cheap (atomic bumps).
    /// `Inline` moves records by value (memcpy on fanout). Pick `Inline`
    /// for embedded / single-consumer deployments where the per-record
    /// Arc::new at drain time is wasteful.
    pub fn record_sharing(mut self, sharing: crate::config::RecordSharing) -> Self {
        self.record_sharing = sharing;
        self
    }

    /// Set the ring overflow policy. `Block` (default) is lossless via elastic
    /// producer-assist — on a full ring the producer drains+exports a batch
    /// itself then pushes, so it never drops and never hangs; `Drop` sheds and
    /// counts under overload. See [`OverflowPolicy`].
    pub fn overflow(mut self, overflow: OverflowPolicy) -> Self {
        self.overflow = overflow;
        self
    }

    /// Spawn an owned background drainer thread at `start()` (default off).
    ///
    /// With it on, steady state has a dedicated pump and producers rarely need
    /// to self-drain under `Block`. Off (the default), the recorder is
    /// bring-your-own-pump: the caller drives `drain()`, and overflowing
    /// producers self-drain via elastic producer-assist. Either way shutdown is
    /// lossless (the final flush runs on teardown). Safe to combine with manual
    /// `drain()` because the rings are multi-consumer.
    ///
    /// No-op (ignored) when the `lossless-backpressure` feature is disabled —
    /// there is no pump infrastructure to spawn.
    pub fn managed_drainer(self, enabled: bool) -> Self {
        #[cfg(feature = "lossless-backpressure")]
        {
            let mut this = self;
            this.managed_drainer = enabled;
            this
        }
        #[cfg(not(feature = "lossless-backpressure"))]
        {
            let _ = enabled;
            self
        }
    }

    /// Records a background drain pass moves+exports per ring per pass. Larger
    /// amortizes the per-pass setup (higher steady-state drain throughput); off
    /// the emit hot path. Defaults to the build-time `sized::DRAIN_BATCH`.
    pub fn drain_batch(mut self, batch: usize) -> Self {
        self.drain_batch = batch.max(1);
        self
    }

    /// Records a producer drains+exports on a full ring (elastic producer-assist)
    /// before retrying its push. This runs on the producer/request thread, so it
    /// bounds the worst-case emit tail (stall ≈ `assist_batch` × per-record sink
    /// latency). Keep it small and well below `drain_batch`. Defaults to the
    /// build-time `sized::DRAIN_ASSIST_BATCH`.
    pub fn assist_batch(mut self, batch: usize) -> Self {
        self.assist_batch = batch.max(1);
        self
    }

    /// How often the managed pump flushes when no ring fills (the time trigger).
    /// The pump sleeps between flushes, so this also sets the idle wake cadence —
    /// smaller bounds export latency, larger spends less CPU at idle. A full ring
    /// still flushes early (the size trigger), independent of this.
    pub fn flush_interval(mut self, interval: Duration) -> Self {
        self.flush_interval = interval;
        self
    }
}

impl RecorderBuilder<HasPipe> {
    /// Override the pipe that `from_config` wired in. Useful in benches and tests
    /// where the structural config (sampler, record_sharing, ring caps) comes from
    /// a `TelemetryConfig` but the terminal sink must be a specific concrete type
    /// (e.g. `InMemoryPipe` for trace benches).
    pub fn with_pipe(
        mut self,
        pipe: impl proxima_primitives::pipe::SendPipe<
            In = crate::pipes::TelemetryRequest,
            Out = proxima_primitives::pipe::request::Response<bytes::Bytes>,
            Err = proxima_primitives::pipe::ProximaError,
        > + 'static,
    ) -> Self {
        self.pipe = Some(into_telemetry_handle(pipe));
        self
    }

    pub fn start(self) -> Result<Recorder, crate::error::Error> {
        let caps = &self.ring_caps;
        let count = self.core_count;

        let mut ring_sets: alloc::vec::Vec<RingSet> = alloc::vec::Vec::with_capacity(count);
        for _ in 0..count {
            ring_sets.push(RingSet::new(caps)?);
        }
        let cores = PerCore::from_vec(ring_sets);

        let pipe = self.pipe.ok_or(crate::error::Error::InvalidInput)?;

        let shared = Arc::new(EmitShared::new(
            cores,
            InstrumentRegistry::new(),
            pipe,
            self.record_sharing,
            self.drain_batch,
            self.assist_batch,
            self.flush_interval,
        ));

        let mut resource = Resource::new();
        for tag in self.resource_tags {
            resource.push_tag(tag);
        }
        resource.freeze();

        let clock: Arc<dyn Clock + Send + Sync> =
            self.clock.unwrap_or_else(|| Arc::new(SystemClock));

        let sampler = Arc::new(
            self.sampler
                .unwrap_or_else(|| alloc::boxed::Box::new(AlwaysOn)),
        );

        #[cfg(feature = "lossless-backpressure")]
        let managed = self
            .managed_drainer
            .then(|| ManagedDrainer::spawn(Arc::clone(&shared)))
            .flatten();

        Ok(Recorder {
            shared,
            resource: Arc::new(resource),
            clock,
            ring_caps: self.ring_caps,
            sampler,
            overflow: self.overflow,
            #[cfg(feature = "lossless-backpressure")]
            managed,
        })
    }
}

struct SystemClock;

impl Clock for SystemClock {
    fn now_ns(&self) -> u64 {
        // Clock::now_ns returns u64 (no Result propagation path). A 0 fallback marks
        // the clock-went-backwards edge case as "before unix epoch" — downstream exporters
        // can detect ts_ns == 0 as a sentinel for a malformed clock if they care to.
        SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .map(|dur| dur.as_nanos() as u64)
            .unwrap_or(0)
    }
}

