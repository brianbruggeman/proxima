    /// Start a span that CONTINUES an inbound W3C trace, given the raw
    /// `traceparent` header bytes (`00-<trace32hex>-<span16hex>-<flags2hex>`).
    ///
    /// The span inherits the parent's `trace_id` and records the parent's span as
    /// `parent_span_id`, with a fresh `span_id` — stitching this service's span
    /// into the distributed trace. Sampling follows the parent's sampled flag
    /// (W3C ParentBased default). A malformed/absent traceparent falls back to a
    /// new root trace via [`span`](Self::span).
    ///
    /// This is the EXTRACT half of W3C propagation; the INJECT half (format a
    /// span's context back into a `traceparent` header) is
    /// `crate::id::format_traceparent` over the span's `trace_id`/`span_id`.
    pub fn span_from_traceparent(
        &self,
        name: &'static str,
        traceparent: &[u8],
    ) -> SpanBuilderWired {
        self.span_from_w3c(name, traceparent, None)
    }

    /// Like [`span_from_traceparent`](Self::span_from_traceparent), but also
    /// carries the inbound W3C `tracestate` header (the vendor key=value list)
    /// onto the span so it propagates unchanged to downstream hops.
    pub fn span_from_w3c(
        &self,
        name: &'static str,
        traceparent: &[u8],
        tracestate: Option<&[u8]>,
    ) -> SpanBuilderWired {
        let Some((trace_id, parent_span_id, flags)) =
            crate::id::parse_traceparent(traceparent)
        else {
            return self.span(name);
        };
        let span_id = SpanId::generate();
        let mut builder = SpanBuilder::new(name, trace_id, span_id).parent(parent_span_id);
        if let Some(state) = tracestate {
            builder = builder.with_tracestate(crate::trace::tracestate::TraceState::from_bytes(
                bytes::Bytes::copy_from_slice(state),
            ));
        }
        // ParentBased: honour the upstream sampling decision.
        let sampled = flags.0 & 0x01 != 0;
        SpanBuilderWired::new(
            builder,
            Arc::clone(&self.shared),
            Arc::clone(&self.clock),
            alloc::vec::Vec::new(),
            sampled,
            self.overflow,
        )
    }

