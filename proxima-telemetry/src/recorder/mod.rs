pub struct Recorder {
    shared: Arc<EmitShared>,
    resource: Arc<Resource>,
    clock: Arc<dyn Clock + Send + Sync>,
    ring_caps: RingCapacities,
    sampler: Arc<alloc::boxed::Box<dyn Sampler>>,
    /// emit-time ring overflow policy. `Block` (default) is lossless via elastic
    /// producer-assist when `lossless-backpressure` is active; degrades to `Drop`
    /// without it. See [`OverflowPolicy`].
    overflow: OverflowPolicy,
    /// optional owned drainer thread (off by default). When present it pumps the
    /// rings in steady state so producers rarely self-drain; stopped + joined on
    /// `Drop` before the final flush.
    /// only present under the `lossless-backpressure` feature.
    #[cfg(feature = "lossless-backpressure")]
    managed: Option<ManagedDrainer>,
}

/// An owned background drainer thread. Spawned by the builder when
/// `managed_drainer(true)` is set; stopped and joined on `Recorder::drop`.
/// Safe to run alongside manual `drain()`/`drain_range()` because the rings are
/// multi-consumer (the MPMC dequeue).
///
/// Only present under the `lossless-backpressure` feature.
#[cfg(feature = "lossless-backpressure")]
struct ManagedDrainer {
    stop: Arc<AtomicBool>,
    shared: Arc<EmitShared>,
    handle: Option<std::thread::JoinHandle<()>>,
}

#[cfg(feature = "lossless-backpressure")]
impl ManagedDrainer {
    /// Spawn the pump. `None` if the OS refuses the thread — the recorder then
    /// degrades to bring-your-own-pump + elastic producer-assist, which is still
    /// lossless, rather than panicking at startup.
    ///
    /// The pump is size-or-time triggered and sleeps between flushes: it parks
    /// until `flush_interval` elapses (time) or a producer signals a full ring
    /// (size), then drains the accumulated batches. An idle recorder therefore
    /// wakes only once per `flush_interval` — no hot-spin burning a core.
    fn spawn(shared: Arc<EmitShared>) -> Option<Self> {
        let stop = Arc::new(AtomicBool::new(false));
        // mark the pump live BEFORE moving the shared into the drainer: a full-ring
        // producer now parks for a freed slot instead of self-exporting.
        shared.set_pump_active(true);
        let drainer = Drainer::new(Arc::clone(&shared));
        let stop_thread = Arc::clone(&stop);
        let pump_shared = Arc::clone(&shared);
        match std::thread::Builder::new()
            .name(alloc::string::String::from("telemetry-drainer"))
            .spawn(move || {
                let flush_size = pump_shared.drain_batch();
                while !stop_thread.load(Ordering::Relaxed) {
                    // sleep until the time trigger (flush_interval) or the size
                    // trigger (a producer signalled a full ring / shutdown).
                    pump_shared.pump_park();
                    // drain the accumulated batch: keep going while each pass
                    // yields a full batch (more is waiting), then the remainder.
                    while !stop_thread.load(Ordering::Relaxed) && drainer.drain_pass() >= flush_size
                    {
                    }
                }
            }) {
            Ok(handle) => Some(Self {
                stop,
                shared,
                handle: Some(handle),
            }),
            Err(error) => {
                // no pump after all — fall back to elastic producer-assist.
                shared.set_pump_active(false);
                tracing::error!(error = %error, "telemetry drainer thread spawn failed; falling back to producer-assist");
                None
            }
        }
    }

    fn stop_and_join(&mut self) {
        self.stop.store(true, Ordering::Relaxed);
        // wake the parked pump so it sees `stop` immediately instead of waiting
        // out the flush_interval.
        self.shared.notify_data_ready();
        if let Some(handle) = self.handle.take() {
            let _ = handle.join();
        }
    }
}

impl Drop for Recorder {
    fn drop(&mut self) {
        // stop the managed pump first (release its EmitShared ref) so the final
        // flush is unraced. the ring + instrument flush itself happens in
        // EmitShared::drop once the last reference (this recorder plus any
        // still-live span guards) is gone — that is where the pipe lives.
        #[cfg(feature = "lossless-backpressure")]
        {
            if let Some(managed) = self.managed.as_mut() {
                managed.stop_and_join();
            }
            // the pump is gone; clear pump-active and wake any producer parked for a
            // freed slot so a late shutdown-time emit falls through to self-assist
            // rather than waiting on a pump that will never run again.
            self.shared.set_pump_active(false);
        }
    }
}

