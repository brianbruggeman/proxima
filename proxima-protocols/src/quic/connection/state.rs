//! Per-variant state structs for [`ConnectionState`].
//!
//! Each variant carries exactly the data that's legal in that state.
//! Transitions consume the old variant (move semantics) and produce
//! the new one; the compiler enforces transition validity by virtue
//! of exhaustive `match`. No `Box<dyn Trait>`, no `Arc<Mutex<State>>`.
//!
//! Stream table, ACK scheduler, loss-detection state, congestion
//! controller, key-update manager, datagram queues, path challenger,
//! multipath table, and ECN state are all live members of the
//! variants below.

