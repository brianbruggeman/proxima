/// Maximum bytes a peer's encoded transport-parameter set can occupy
/// in the connection state stash. Sized to comfortably hold a full
/// 18-parameter set including preferred-address (~150 B in practice).
pub const PEER_TRANSPORT_PARAMS_INLINE_BYTES: usize = 256;

/// Maximum CRYPTO bytes the connection buffers between TLS provider
/// calls and packetisation. A real QUIC ClientHello (ALPN + transport
/// params + key share) is ~1514 B and the server's first flight (cert +
/// cert-verify) is larger, so one MTU does NOT bound a single flight; the
/// transmit side fragments the buffered crypto across Initial datagrams.
/// Sized to comfortably hold a flight with a small (self-signed) cert.
pub const CRYPTO_INLINE_BYTES: usize = 4096;

/// Const-generic cap on issued / received connection IDs per RFC 9000
/// §18.2 `active_connection_id_limit`. Sourced from
/// `proxima-quic-proto.toml [connection].cid_queue_cap` (override via
/// `PROXIMA_QUIC_PROTO_CONNECTION_CID_QUEUE_CAP`).
pub const CID_QUEUE_CAP: usize = crate::quic::sized::CONNECTION_CID_QUEUE_CAP;

/// Per-connection cap on concurrently-active bidirectional streams.
/// Sourced from `proxima-quic-proto.toml [streams].max_concurrent_bidi`
/// (override via `PROXIMA_QUIC_PROTO_STREAMS_MAX_CONCURRENT_BIDI`).
///
/// Each stream carries inline send + recv buffers — raising this also
/// raises the dispatcher's stack footprint roughly linearly. Tune in
/// concert with [`crate::quic::streams::STREAM_SEND_INLINE`] +
/// [`crate::quic::streams::STREAM_RECV_INLINE`] for memory-constrained
/// targets.
pub const MAX_BIDI_STREAMS: usize = crate::quic::sized::STREAMS_MAX_CONCURRENT_BIDI;

/// Per-connection cap on concurrently-active unidirectional streams.
/// Sourced from `proxima-quic-proto.toml [streams].max_concurrent_uni`.
pub const MAX_UNI_STREAMS: usize = crate::quic::sized::STREAMS_MAX_CONCURRENT_UNI;

/// Fixed cap for the readable-stream set. Every readable stream lives in
/// the table (bounded by the concurrent caps); a reaped stream can linger
/// one drain cycle, so 2x the table cap is safe headroom and keeps the set
/// alloc-free — a `heapless::Vec` on the stack, not a `BTreeSet` that
/// allocates a tree node per readable stream every poll.
pub const READABLE_CAP: usize = (MAX_BIDI_STREAMS + MAX_UNI_STREAMS) * 2;

/// Connection identifier byte string.
pub type ConnectionIdBytes = ArrayVec<u8, MAX_CID_LEN>;

/// Inline byte stash for the peer's encoded transport parameters.
/// Populated when the TLS provider fires
/// [`crate::quic::tls::TlsEvent::PeerTransportParameters`].
pub type PeerTransportParametersBytes = ArrayVec<u8, PEER_TRANSPORT_PARAMS_INLINE_BYTES>;

/// Inline byte stash for outbound CRYPTO frame bytes pending packetisation.
pub type CryptoSendBuffer = ArrayVec<u8, CRYPTO_INLINE_BYTES>;

/// Per-epoch CRYPTO frame send-buffer + offset cursors for retransmit
/// support per RFC 9002 §6.2.4. Distinct from [`CryptoSendBuffer`]:
/// this holds ALL bytes generated by the TLS provider that have not
/// yet been ACKed (rather than just the next chunk to emit), and
/// tracks how many bytes have already gone out so retransmit
/// (resend the unacked prefix) is just a cursor reset.
#[derive(Debug, Clone, Default)]
pub struct CryptoEpochBuffer {
    /// Bytes for this epoch's CRYPTO frame stream that haven't yet
    /// been acknowledged. buffer[0] sits at stream offset
    /// [`Self::offset_acked`].
    buffer: ArrayVec<u8, CRYPTO_INLINE_BYTES>,
    /// Stream offset of `buffer[0]`. Advanced as the head of the
    /// buffer gets ACKed.
    offset_acked: u64,
    /// Number of buffer bytes already emitted in at least one outbound
    /// packet. Reset to 0 by loss detection to trigger retransmit.
    bytes_sent: usize,
    /// Per-PN tracking: PN → (start_in_buffer, length_emitted) for
    /// the bytes carried by that packet. Capped at 16 in-flight
    /// CRYPTO packets per epoch — handshake CRYPTO flights rarely
    /// exceed a couple of packets.
    inflight: heapless::LinearMap<u64, CryptoInflight, 16>,
}

/// One in-flight CRYPTO frame tracking record.
#[derive(Debug, Clone, Copy)]
pub struct CryptoInflight {
    /// Offset within the per-epoch buffer where the bytes carried by
    /// this packet started.
    pub start_in_buffer: usize,
    /// Number of bytes emitted in this packet's CRYPTO frame.
    pub length: usize,
}

impl CryptoEpochBuffer {
    /// Construct an empty buffer.
    #[must_use]
    pub const fn new() -> Self {
        Self {
            buffer: ArrayVec::new_const(),
            offset_acked: 0,
            bytes_sent: 0,
            inflight: heapless::LinearMap::new(),
        }
    }

    /// Total bytes currently buffered (acked-prefix already dropped).
    #[must_use]
    pub fn buffered_len(&self) -> usize {
        self.buffer.len()
    }

    /// `true` if no bytes are currently buffered (either never pumped
    /// or every byte has been acknowledged).
    #[must_use]
    pub fn is_empty(&self) -> bool {
        self.buffer.is_empty()
    }

    /// Borrow the full buffer (acked-prefix-stripped) for tests +
    /// diagnostics. NOT the same as [`Self::unsent`] — this includes
    /// bytes already emitted in at least one in-flight packet.
    #[must_use]
    pub fn as_slice(&self) -> &[u8] {
        &self.buffer
    }

    /// Stream offset of the next byte that hasn't been emitted yet.
    #[must_use]
    pub fn next_send_offset(&self) -> u64 {
        self.offset_acked + self.bytes_sent as u64
    }

    /// Bytes ready to send (i.e. not yet emitted). Borrowed slice.
    #[must_use]
    pub fn unsent(&self) -> &[u8] {
        &self.buffer[self.bytes_sent..]
    }

    /// Append fresh bytes from the TLS provider's `write_handshake`.
    /// Returns the number of bytes accepted (caller's error path
    /// fires `BufferTooSmall` when this is less than `bytes.len()`).
    pub fn append(&mut self, bytes: &[u8]) -> usize {
        let cap = self.buffer.remaining_capacity();
        let take = core::cmp::min(cap, bytes.len());
        // try_extend_from_slice is infallible — bounded by `take`.
        let _ = self.buffer.try_extend_from_slice(&bytes[..take]);
        take
    }

    /// Mark `length` bytes (starting at `next_send_offset`) as
    /// emitted in packet `pn`. Returns the stream offset of the
    /// first emitted byte (which the caller writes into the
    /// CRYPTO frame's offset field).
    pub fn record_emission(&mut self, pn: u64, length: usize) -> u64 {
        let start = self.bytes_sent;
        let stream_offset = self.offset_acked + start as u64;
        self.bytes_sent = self.bytes_sent.saturating_add(length);
        if self.inflight.len() >= self.inflight.capacity()
            && let Some((&oldest_pn, _)) = self.inflight.iter().next()
        {
            let _ = self.inflight.remove(&oldest_pn);
        }
        let _ = self.inflight.insert(
            pn,
            CryptoInflight {
                start_in_buffer: start,
                length,
            },
        );
        stream_offset
    }

    /// Apply an ACK for packet `pn`. If the acked packet covered the
    /// head of the buffer, the prefix is dropped and `offset_acked`
    /// advances. Inflight entries for `pn` are removed regardless.
    pub fn on_pn_acked(&mut self, pn: u64) {
        let Some(record) = self.inflight.remove(&pn) else {
            return;
        };
        if record.start_in_buffer == 0 {
            let drop_len = record.length.min(self.buffer.len());
            // Truncate from the front via shift; arrayvec doesn't have
            // a cheap front-drop, so we copy the tail forward.
            self.buffer.drain(..drop_len);
            self.offset_acked = self.offset_acked.saturating_add(drop_len as u64);
            self.bytes_sent = self.bytes_sent.saturating_sub(drop_len);
            // Adjust every other inflight entry's start_in_buffer by
            // -drop_len so they remain consistent with the truncated
            // buffer.
            let pns: arrayvec::ArrayVec<u64, 16> = self.inflight.keys().copied().collect();
            for key in pns {
                if let Some(entry) = self.inflight.get_mut(&key) {
                    entry.start_in_buffer = entry.start_in_buffer.saturating_sub(drop_len);
                }
            }
        }
    }

    /// Apply a loss for packet `pn`. Conservative: reset
    /// `bytes_sent` to the start_in_buffer of the lost packet so
    /// the next emission re-sends those bytes (and everything
    /// after them) — matches RFC 9002 §6.2.4 ("retransmit existing
    /// data, possibly the entire ClientHello").
    pub fn on_pn_lost(&mut self, pn: u64) {
        if let Some(record) = self.inflight.remove(&pn) {
            self.bytes_sent = self.bytes_sent.min(record.start_in_buffer);
        }
    }

    /// PTO probe: reset the send cursor to 0 so the next emission
    /// re-sends the entire CRYPTO flight. RFC 9002 §6.2.4:
    /// "retransmit existing data, possibly the entire ClientHello."
    pub fn reset_for_pto(&mut self) {
        self.bytes_sent = 0;
    }

    /// `true` if there are bytes either pending first send or
    /// pending retransmit (after a loss-reset).
    #[must_use]
    pub fn has_unsent(&self) -> bool {
        self.bytes_sent < self.buffer.len()
    }
}

/// Upper bound on a single epoch's inbound CRYPTO stream offset+len. A
/// ClientHello (server side) or Certificate flight (client side) sits
/// well under this; the cap only fences a peer that names an absurd
/// offset. Lives inside the handshake-phase state, so the whole
/// reassembler is freed at Established — bounded by concurrent
/// handshakes, not total connection count.
pub const CRYPTO_RECV_BYTES: usize = 8192;

/// In-order reassembly for one epoch's inbound CRYPTO stream (RFC 9000
/// §19.6). CRYPTO frames carry a stream offset and may arrive out of
/// order or span several packets; the TLS provider must be fed
/// contiguous bytes from offset 0.
///
/// Out-of-order fragments live in a `BTreeMap` keyed by their start
/// offset — the key *is* the order, so reassembly looks up the fragment
/// covering the next expected byte directly; there is no sort pass.
#[derive(Debug, Clone, Default)]
pub struct CryptoRecvBuffer {
    /// Pending fragments keyed by start offset. The map keeps keys
    /// ordered for free; we index by offset, never sort.
    pending: BTreeMap<u64, Vec<u8>>,
    /// Contiguous prefix already assembled and handed to the provider
    /// (index == stream offset). Retained as the backing for the slice
    /// returned by [`Self::accept`]; its length is the next expected
    /// offset.
    head: Vec<u8>,
}

impl CryptoRecvBuffer {
    /// Construct an empty reassembler.
    #[must_use]
    pub const fn new() -> Self {
        Self {
            pending: BTreeMap::new(),
            head: Vec::new(),
        }
    }

    /// Stage a CRYPTO fragment at `offset` and return the run of
    /// newly-contiguous bytes ready for the TLS provider, if any. A
    /// fragment naming an offset past [`CRYPTO_RECV_BYTES`] is dropped;
    /// the peer retransmits unacked CRYPTO so nothing is lost.
    pub fn accept(&mut self, offset: u64, data: &[u8]) -> Option<&[u8]> {
        let delivered = self.head.len() as u64;
        let end = offset.saturating_add(data.len() as u64);
        if data.is_empty() || end <= delivered || end > CRYPTO_RECV_BYTES as u64 {
            return None;
        }
        // Clip any prefix already in the contiguous head, then stash the
        // remainder under its (clipped) offset key.
        let start = offset.max(delivered);
        let head_already = (start - offset) as usize;
        self.pending.insert(start, data[head_already..].to_vec());

        let before = self.head.len();
        // Drain every fragment that now extends the contiguous prefix.
        // `range(..=delivered)` finds the fragment covering the next
        // byte without scanning or sorting.
        loop {
            let next = self.head.len() as u64;
            let Some((&frag_start, _)) = self.pending.range(..=next).next_back() else {
                break;
            };
            let Some(frag) = self.pending.remove(&frag_start) else {
                break;
            };
            let frag_end = frag_start + frag.len() as u64;
            if frag_end <= next {
                continue; // wholly covered already — drop
            }
            let from = (next - frag_start) as usize;
            self.head.extend_from_slice(&frag[from..]);
        }
        if self.head.len() > before {
            Some(&self.head[before..])
        } else {
            None
        }
    }
}

